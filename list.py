fruits=['apple','banana','mango','orange','pineapple']

# checking the type of class
print(type(fruits))

# displaying one of the fruits in the list
print(fruits[-2])

# using index to change
fruits[4]='lemon'
print(fruits)

# slicing
y=fruits[2:]
print(y)


# slicing and reversing
x=fruits[::-1]
print(x)

